package ReadExcelData;

public class Encapsulationdemo {
   private String empName;
   private int empAge;
	
   public String getEmpName()
   {
	   return empName;
   }
   public int getEmpAge()
   {
	   return empAge;
   }
   public void setEmpName(String newValue)
   {
	    empName=newValue;
   }
   public void setEmpAge(int newValue)
   {
	   empAge=newValue;
   }
}
 
